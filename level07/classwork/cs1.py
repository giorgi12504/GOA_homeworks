#შექმენით პროგრამა რომელიც მომხმარებელს მოთხოვს რაიმე ტექსტს და შემდეგ რიცხვს. პროგრამამ უნდა გამოიტანოს ამ ტექსტის შესაბამისი ასო რიცსვის მიხედვით

text = input("გთხოვთ, შეიყვანოთ ტექსტი: ")
index = int(input("გთხოვთ, შეიყვანოთ რიცხვი (1-დან დასაწყისი): "))


count = 0
for char in text:
    count += 1


if index >= 1 and index <= count:
   
    print("მათითებელი ასო არის:", text[index - 1])
else:
    print("შეყვანილი რიცხვი გადადის ტექსტის სიგრძეზე!")
