# შემოაყვანინეთ მომხმარებელს ორი რიცხვი, და გამოპრინტეთ ამ ორი რიცხვის გაერთიანებულ ვერსისას დამატებული 1, მაგ თუ ჯერ შემოვიდა ათი შემდეგ 11, თქვენი მიზანია გამოიტანოთ 1012, ვინაიდან 10 გაერთანებული 11 არის 1011 და 1011 + 1 არის 1012

# ორი რიცხვის შეყვანა
num1 = input("მიუთითეთ პირველი რიცხვი: ")
num2 = input("მიუთითეთ მეორე რიცხვი: ")

# რიცხვების გაერთიანება
combined = num1 + num2

# გაერთიანებულ რიცხვში 1-ის დამატება
result = int(combined) + 1

# შედეგის გამოშვება
print("გაერთიანებული რიცხვი + 1 არის:", result)